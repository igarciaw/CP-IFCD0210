// 13-convierte-pulgadas-a-centimetros
// Escriba un algoritmo que convierta las medidas de pulgadas a centímetros, teniendo en cuenta que una pulgada son 2,54cm.

let pulgadas = parseInt(prompt('Introduce las pulgadas: '));
let centimetros = pulgadas * 2.54;

alert(pulgadas + ' pulgadas en centímetros son: '+ centimetros + 'cm.');
console.log(pulgadas + ' pulgadas en centímetros son: '+ centimetros + 'cm.');
